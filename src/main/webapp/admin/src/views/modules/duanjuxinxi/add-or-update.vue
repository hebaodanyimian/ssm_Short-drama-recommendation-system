	import { 
		isNumber,
		isIntNumer,
	} from "@/utils/validate";
	export default {
		data() {
			var validateNumber = (rule, value, callback) => {
				if(!value){
					callback();
				} else if (!isNumber(value)) {
					callback(new Error("请输入数字"));
				} else {
					callback();
				}
			};
			var validateIntNumber = (rule, value, callback) => {
				if(!value){
					callback();
				} else if (!isIntNumer(value)) {
					callback(new Error("请输入整数"));
				} else {
					callback();
				}
			};
			return {
				id: '',
				type: '',
			
			
				ro:{
					duanjubiaoti : false,
					duanjufenlei : false,
					duanjutupian : false,
					guankanrenshu : false,
					fabushijian : false,
					duanjujianjie : false,
					duanjuxiangqing : false,
					thumbsupnum : false,
					crazilynum : false,
					clicktime : false,
					clicknum : false,
					discussnum : false,
					totalscore : false,
					storeupnum : false,
				},
			
				ruleForm: {
					duanjubiaoti: '',
					duanjufenlei: '',
					duanjutupian: '',
					guankanrenshu: '',
					fabushijian: '',
					duanjujianjie: '',
					duanjuxiangqing: '',
					clicktime: '',
				},
				duanjufenleiOptions: [],

				rules: {
					duanjubiaoti: [
					],
					duanjufenlei: [
					],
					duanjutupian: [
					],
					guankanrenshu: [
						{ validator: validateIntNumber, trigger: 'blur' },
					],
					fabushijian: [
					],
					duanjujianjie: [
					],
					duanjuxiangqing: [
					],
					thumbsupnum: [
						{ validator: validateIntNumber, trigger: 'blur' },
					],
					crazilynum: [
						{ validator: validateIntNumber, trigger: 'blur' },
					],
					clicktime: [
					],
					clicknum: [
						{ validator: validateIntNumber, trigger: 'blur' },
					],
					discussnum: [
						{ validator: validateIntNumber, trigger: 'blur' },
					],
					totalscore: [
						{ validator: validateNumber, trigger: 'blur' },
					],
					storeupnum: [
						{ validator: validateIntNumber, trigger: 'blur' },
